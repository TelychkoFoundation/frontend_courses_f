export default function transliterate(text: string): string {
  const alphabetMap: { [key: string]: string } = {
    А: "A",
    Б: "B",
    В: "V",
    Г: "H",
    Д: "D",
    Е: "E",
    Є: "YE",
    Ж: "ZH",
    З: "Z",
    И: "Y",
    І: "I",
    Ї: "YI",
    Й: "Y",
    К: "K",
    Л: "L",
    М: "M",
    Н: "N",
    О: "O",
    П: "P",
    Р: "R",
    С: "S",
    Т: "T",
    У: "U",
    Ф: "F",
    Х: "KH",
    Ц: "TS",
    Ч: "CH",
    Ш: "SH",
    Щ: "SCH",
    Ь: "",
    Ю: "YU",
    Я: "YA",
    а: "a",
    б: "b",
    в: "v",
    г: "h",
    д: "d",
    е: "e",
    є: "ye",
    ж: "zh",
    з: "z",
    и: "y",
    і: "i",
    ї: "yi",
    й: "y",
    к: "k",
    л: "l",
    м: "m",
    н: "n",
    о: "o",
    п: "p",
    р: "r",
    с: "s",
    т: "t",
    у: "u",
    ф: "f",
    х: "kh",
    ц: "ts",
    ч: "ch",
    ш: "sh",
    щ: "sch",
    ь: "",
    ю: "yu",
    я: "ya",
    "'": "",
    " ": "-",
  };

  return text
    .split("")
    .map(letter => alphabetMap[letter] || letter)
    .join("")
    .toLowerCase()
    .replace(/[^a-z0-9\-]/g, "");
}
